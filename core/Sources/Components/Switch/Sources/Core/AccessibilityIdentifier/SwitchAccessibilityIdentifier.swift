/// The accessibility identifiers for the switch.
public enum SwitchAccessibilityIdentifier {

    // MARK: - Properties

    /// The toggle view accessibility identifier.
    public static let toggleView = "spark-switch-toggleView"
    /// The toggle dot view accessibility identifier.
    public static let toggleDotView = "spark-switch-toggle-dotView"
    /// The toggle dot image view accessibility identifier.
    public static let toggleDotImageView = "spark-switch-toggle-dotImageView"
    /// The text accessibility identifier.
    public static let text = "spark-switch-text"
}
